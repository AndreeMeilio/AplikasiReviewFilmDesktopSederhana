    public FilmFrame() {
        initComponents();
        this.setExtendedState(JFrame.MAXIMIZED_BOTH);
        film = new Film();
        
        List<Film> lfilm = film.select_data();
        List<GenreFilm> lgf = film.getGenre_film().select_data();
        
        DefaultTableModel tblModel = (DefaultTableModel) tblFilm.getModel();
        
        for (int i = 0; i < lfilm.size(); i++){
            String id_film = String.valueOf(lfilm.get(i).getId_film());
            String judul_film = String.valueOf(lfilm.get(i).getJudul_film());
            String tanggal_publish = String.valueOf(lfilm.get(i).getTanggal_publish());
            String url_video = String.valueOf(lfilm.get(i).getUrl_video());
            String genre_film = "";
                            
            for (int j = 0; j < lgf.size(); j++){
                if (lfilm.get(i).getId_genre_film() == lgf.get(j).getId_genre_film()){
                    genre_film = lgf.get(j).getNama_genre_film();
                }
            }
            
            String filmData[] = {id_film, judul_film, tanggal_publish, genre_film, url_video};
            tblModel.addRow(filmData);
            
        }
    }
   
    
