    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        int coloum = 0;
        int row = tblFilm.getSelectedRow();
                
        if (row != -1){
            int hapus_data = JOptionPane.showConfirmDialog(this, "Apa Anda Yakin Ingin Menghapus Data Ini??", "Menghapus Data", JOptionPane.WARNING_MESSAGE);
            
            if (hapus_data == 0){
                int id_film = Integer.parseInt(tblFilm.getValueAt(row, coloum).toString());
                
                Film film = new Film();
                film.setId_film(id_film);
                
                boolean isDeleted = film.delete_data(film);
                
                if (isDeleted){
                    JOptionPane.showMessageDialog(this, "Delete Data Successfully", "Delete Data", JOptionPane.INFORMATION_MESSAGE);
                    
                    this.setVisible(false);
                    new FilmFrame().setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        
        int coloum = 0;
        int row = tblFilm.getSelectedRow();
        
        if (row != -1){
            int id_film = Integer.parseInt(tblFilm.getValueAt(row, coloum).toString());
            
            Film film = new Film();
            film.setId_film(id_film);
            
            Film film_result = film.select_data_with_id(film);
            
            new EditFilmFrame(film_result).setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btn_editActionPerformed

